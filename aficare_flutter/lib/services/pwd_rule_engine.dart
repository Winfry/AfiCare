import '../models/disability_profile.dart';

// ----------------------------------------------------------------
// Recommendation categories
// ----------------------------------------------------------------

enum RecommendationPriority {
  /// Act immediately — safety risk
  critical,
  /// Act at this visit
  high,
  /// Act this month
  medium,
  /// General monitoring
  low,
}

enum RecommendationCategory {
  healthCheck,    // Screening / physical examination
  labTest,        // Blood, urine, imaging
  medication,     // Medication management
  referral,       // Specialist referral
  lifestyle,      // Daily living, devices, habits
  providerNote,   // Instruction for the treating clinician
  caregiverAlert, // Alert/task for the caregiver
}

// ----------------------------------------------------------------
// Recommendation data class
// ----------------------------------------------------------------

class PwdRecommendation {
  /// Stable unique identifier — this is the key for future ML swap.
  /// Convention: '{disabilityType}_{short_name}'
  final String id;

  final String title;
  final String description;
  final RecommendationPriority priority;
  final RecommendationCategory category;
  final DisabilityType triggerType;

  /// true  → generated by a hard-coded rule (current behaviour)
  /// false → generated by an ML model (future)
  final bool isRuleBased;

  /// Optional label for an action button, e.g. 'Book appointment'
  final String? actionLabel;

  // Future ML fields (uncomment when ML is ready):
  // final double? mlConfidence;
  // final String? mlModelVersion;

  const PwdRecommendation({
    required this.id,
    required this.title,
    required this.description,
    required this.priority,
    required this.category,
    required this.triggerType,
    this.isRuleBased = true,
    this.actionLabel,
  });
}

