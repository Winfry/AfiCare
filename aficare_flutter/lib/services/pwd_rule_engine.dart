  // Public API
  // ----------------------------------------------------------

  /// Returns all applicable recommendations for [profile],
  /// de-duplicated and sorted by priority (critical → low).
  ///
  /// Severity modifier:
  ///   mild     → critical + high only
  ///   moderate → critical + high + medium
  ///   severe   → all (critical + high + medium + low)
  List<PwdRecommendation> getRecommendations(DisabilityProfile profile) {
    if (profile.isEmpty) return [];

    final seen = <String>{};
    final results = <PwdRecommendation>[];

    for (final type in profile.disabilityTypes) {
      for (final rec in _rulesFor(type, profile)) {
        if (seen.add(rec.id)) {
          results.add(rec);
        }
      }
    }

    // Apply severity filter
    final filtered = results.where((r) {
      switch (profile.severity) {
        case DisabilitySeverity.mild:
          return r.priority == RecommendationPriority.critical ||
              r.priority == RecommendationPriority.high;
        case DisabilitySeverity.moderate:
          return r.priority != RecommendationPriority.low;
        case DisabilitySeverity.severe:
          return true;
      }
    }).toList();

    // Sort: critical first, then high, medium, low
    filtered.sort((a, b) => a.priority.index.compareTo(b.priority.index));
    return filtered;
  }

  /// Returns provider-facing notes only — used inside consultation screen.
  List<PwdRecommendation> getProviderNotes(DisabilityProfile profile) =>
      getRecommendations(profile)
          .where((r) =>
              r.category == RecommendationCategory.providerNote ||
              r.category == RecommendationCategory.caregiverAlert)
          .toList();

  /// Returns recommended specialist referrals for [profile].
  List<String> getSuggestedReferrals(DisabilityProfile profile) =>
      getRecommendations(profile)
          .where((r) => r.category == RecommendationCategory.referral)
          .map((r) => r.title)
          .toSet()
          .toList();

  // ----------------------------------------------------------
  // Internal — rules per disability type
  // ----------------------------------------------------------

  List<PwdRecommendation> _rulesFor(
    DisabilityType type,
    DisabilityProfile profile,
  ) {
    switch (type) {
      case DisabilityType.visual:
        return _visualRules(profile);
      case DisabilityType.hearing:
        return _hearingRules(profile);
      case DisabilityType.mobility:
        return _mobilityRules(profile);
      case DisabilityType.cognitive:
        return _cognitiveRules(profile);
      case DisabilityType.speech:
        return _speechRules(profile);
      case DisabilityType.chronicIllness:
        return _chronicIllnessRules(profile);
      case DisabilityType.mentalHealth:
        return _mentalHealthRules(profile);
      case DisabilityType.multiple:
        // For 'multiple', run rules for all common combinations
        return [
          ..._visualRules(profile),
          ..._mobilityRules(profile),
          ..._cognitiveRules(profile),
        ];
    }
  }

